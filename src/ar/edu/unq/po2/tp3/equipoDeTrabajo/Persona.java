package ar.edu.unq.po2.tp3.equipoDeTrabajo;

public class Persona {
	
	private String nombre,apellido;
	private int edad;
	
	public Persona(String nombre, String apellido, int edad) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.edad = edad;
	}

	public int getEdad() {
		return edad;
	}
	
	
	
	

}
